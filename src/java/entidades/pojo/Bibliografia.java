
package entidades.pojo;


public class Bibliografia {
    
   private Integer pk_bibliografia ;
   private String descricao_bibliografica;
   private Integer id_disciplina;
   private String tipo_bibliografica;

    public Integer getPk_bibliografia() {
        return pk_bibliografia;
    }

    public void setPk_bibliografia(Integer pk_bibliografia) {
        this.pk_bibliografia = pk_bibliografia;
    }

    public String getDescricao_bibliografica() {
        return descricao_bibliografica;
    }

    public void setDescricao_bibliografica(String descricao_bibliografica) {
        this.descricao_bibliografica = descricao_bibliografica;
    }

    public Integer getId_disciplina() {
        return id_disciplina;
    }

    public void setId_disciplina(Integer id_disciplina) {
        this.id_disciplina = id_disciplina;
    }

    public String getTipo_bibliografica() {
        return tipo_bibliografica;
    }

    public void setTipo_bibliografica(String tipo_bibliografica) {
        this.tipo_bibliografica = tipo_bibliografica;
    }

 

 
   
   
   
    
}
